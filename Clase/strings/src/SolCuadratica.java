public class SolCuadratica {
    private Float solucion1, solucion2;

    public String toString() {
        return "La solución 1 es: " + solucion1 + " y la solución 2 es: " + solucion2;
    }

    // Getters y setters

    public Float getSolucion1() {
        return this.solucion1;
    }

    public void setSolucion1(float d) {
        this.solucion1 = d;
    }

    public Float getSolucion2() {
        return this.solucion2;
    }

    public void setSolucion2(Float solucion2) {
        this.solucion2 = solucion2;
    }

}
